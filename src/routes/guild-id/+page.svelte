<p>You will need the <strong>full Server ID</strong> (not the name).</p>

<p>The Guild ID is a string of 18 numerals (eg. 889493760197668924).</p>

<p>Follow these steps to find your Server ID:</p>

<ol>
  <li>
    In Discord, open your User Settings by clicking the Settings Cog next to
    your user name on the bottom.
  </li>
  <li>
    Go to Appearance and enable Developer Mode under the Advanced section, then
    close User Settings.
  </li>
  <li>
    Open your Discord server, right-click on the server name, then select Copy
    ID
  </li>
  <li>If desired, you can disable Developer Mode now.</li>
</ol>
